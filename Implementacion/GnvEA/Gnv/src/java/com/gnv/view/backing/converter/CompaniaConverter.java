package com.gnv.view.backing.converter;

import com.gnv.business.ejb.modelo.Compania;
import com.gnv.business.ejb.persistencia.CompaniaFacadeLocal;
import javax.ejb.EJB;
import javax.inject.Named;
import javax.enterprise.context.Dependent;
import javax.faces.application.FacesMessage;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.Converter;
import javax.faces.convert.ConverterException;

/**
 *
 * @author Administrador
 */
@Named(value = "companiaConverter")
@Dependent
public class CompaniaConverter implements Converter{

    @EJB
    private CompaniaFacadeLocal companiaFacade;
    
    public CompaniaConverter()
    {
    }
    @Override
    public Object getAsObject(FacesContext context, UIComponent component, String value)
    {
        if (value == null || value.isEmpty() || value.equals("Seleccione"))
        {
            return null;
        }
        try
        {
            int i = Integer.valueOf(value);
            Compania c = companiaFacade.find(i);
            return c;
        } catch (Exception e)
        {
            throw new ConverterException(new FacesMessage(FacesMessage.SEVERITY_ERROR, "Error de conversión", "Objeto no válido"));
        }
    }

    @Override
    public String getAsString(FacesContext context, UIComponent component, Object value)
    {
        if (!(value instanceof Compania))
        {
            return null;
        }
        return String.valueOf(((Compania) value).getId());
    }
}
